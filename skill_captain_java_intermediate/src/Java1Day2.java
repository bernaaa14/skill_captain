public class Java1Day2 {
    public static void main(String[] args) {
        int randomInt= 14;
        boolean randomBoolean= true;
        char randomChar = 'B';
        double randomDouble = 10.90;
        long randomLong = 341223234;
        byte randomByte = 127;
        short randomShort = 3245;
        float randomFloat = 3.14f;

        System.out.println(randomInt);
        System.out.println(randomBoolean);
        System.out.println(randomChar);
        System.out.println(randomDouble);
        System.out.println(randomLong);
        System.out.println(randomByte);
        System.out.println(randomShort);
        System.out.println(randomFloat);
    }
}
